    private void EnterBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterBookButtonActionPerformed
        // Enter Book info Into Database

        if (("".equals(TitleTextField.getText())) || ("".equals(ANameField.getText())) || ("".equals(GenreField.getText()))) {
            JOptionPane.showMessageDialog(null, "You have forgotten one or more required fields");
        } else {
            try {

                dbConnectionManager.getConnection();

                String Title = TitleTextField.getText();
                String AuthorName = ANameField.getText() ;
                String BookGenre = GenreField.getText();
                String BookStatus = "In";

//                java.sql.Statement stmt = dbConnectionManager.getConnection().createStatement();
                String sql = String.format("INSERT INTO book (Title, AuthorName, Genre, BookStatus) VALUES ('" +  Title + "','" +  AuthorName + "','" +  BookGenre + "','" +  BookStatus + "')");
                java.sql.Statement stmt = dbConnectionManager.getConnection().prepareStatement(sql);
                stmt.executeUpdate(sql);
                
                //Display Success Message
                MessagePane.setText(TitleTextField.getText() + " entered successfully!");

                // Clear All Form Fields
                TitleTextField.setText("");
                ANameField.setText("");
                GenreField.setText("");
                TitleTextField.requestFocus();
                dbConnectionManager.getConnection().close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

    }//GEN-LAST:event_EnterBookButtonActionPerformed

